package org.anbin_aravaippu_arakkattali.Anbin.Aravanaippu.Arakkattalai.Entity;

public class VolunteerRegistration {

	private String volunteerName;
	private String volunteerAge;
	private String volunteerEmail;
	private String volunteerPhone;
	private String volunteerDistrict;
	private String volunteerBloodGroup;
	private Boolean workTogether;
	private Boolean bloodDonate;

	public String getVolunteerName() {
		return volunteerName;
	}

	public void setVolunteerName(String volunteerName) {
		this.volunteerName = volunteerName;
	}

	public String getVolunteerAge() {
		return volunteerAge;
	}

	public void setVolunteerAge(String volunteerAge) {
		this.volunteerAge = volunteerAge;
	}

	public String getVolunteerEmail() {
		return volunteerEmail;
	}

	public void setVolunteerEmail(String volunteerEmail) {
		this.volunteerEmail = volunteerEmail;
	}

	public String getVolunteerPhone() {
		return volunteerPhone;
	}

	public void setVolunteerPhone(String volunteerPhone) {
		this.volunteerPhone = volunteerPhone;
	}

	public String getVolunteerDistrict() {
		return volunteerDistrict;
	}

	public void setVolunteerDistrict(String volunteerDistrict) {
		this.volunteerDistrict = volunteerDistrict;
	}

	public String getVolunteerBloodGroup() {
		return volunteerBloodGroup;
	}

	public void setVolunteerBloodGroup(String volunteerBloodGroup) {
		this.volunteerBloodGroup = volunteerBloodGroup;
	}

	public Boolean getWorkTogether() {
		return workTogether;
	}

	public void setWorkTogether(Boolean workTogether) {
		this.workTogether = workTogether;
	}

	public Boolean getBloodDonate() {
		return bloodDonate;
	}

	public void setBloodDonate(Boolean bloodDonate) {
		this.bloodDonate = bloodDonate;
	}

}
