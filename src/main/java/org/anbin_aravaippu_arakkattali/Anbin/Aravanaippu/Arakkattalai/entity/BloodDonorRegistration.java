package org.anbin_aravaippu_arakkattali.Anbin.Aravanaippu.Arakkattalai.Entity;

public class BloodDonorRegistration {

	private String bloodDonorName;
	private String bloodDonorAge;
	private String bloodGroup;
	private String bloodDonorPhonoNo;
	private String bloodDonorEmail;
	private String bloodDonorDistrict;
	private Boolean bloodDonoteContact;

	public String getBloodDonorName() {
		return bloodDonorName;
	}

	public void setBloodDonorName(String bloodDonorName) {
		this.bloodDonorName = bloodDonorName;
	}

	public String getBloodDonorAge() {
		return bloodDonorAge;
	}

	public void setBloodDonorAge(String bloodDonorAge) {
		this.bloodDonorAge = bloodDonorAge;
	}

	public String getBloodGroup() {
		return bloodGroup;
	}

	public void setBloodGroup(String bloodGroup) {
		this.bloodGroup = bloodGroup;
	}

	public String getBloodDonorPhonoNo() {
		return bloodDonorPhonoNo;
	}

	public void setBloodDonorPhonoNo(String bloodDonorPhonoNo) {
		this.bloodDonorPhonoNo = bloodDonorPhonoNo;
	}

	public String getBloodDonorEmail() {
		return bloodDonorEmail;
	}

	public void setBloodDonorEmail(String bloodDonorEmail) {
		this.bloodDonorEmail = bloodDonorEmail;
	}

	public String getBloodDonorDistrict() {
		return bloodDonorDistrict;
	}

	public void setBloodDonorDistrict(String bloodDonorDistrict) {
		this.bloodDonorDistrict = bloodDonorDistrict;
	}

	public Boolean getBloodDonoteContact() {
		return bloodDonoteContact;
	}

	public void setBloodDonoteContact(Boolean bloodDonoteContact) {
		this.bloodDonoteContact = bloodDonoteContact;
	}

}
